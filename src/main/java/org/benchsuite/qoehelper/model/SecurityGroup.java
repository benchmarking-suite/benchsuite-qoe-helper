package org.benchsuite.qoehelper.model;


public class SecurityGroup {

  private String id;
  private String name;

  public SecurityGroup() {
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
